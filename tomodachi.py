    def _start_tick(self):
        self._stop_tick()
        self._tick()

    def _stop_tick(self):
        if self._timer:
            self._timer.cancel()
            self._timer = None

    def _tick(self):
        if self.pet and not self.pet.is_dead:
            self.logic.tick()
            self._show()
            if time.time() - self._last_autosave >= AUTOSAVE_INTERVAL:
                save_pet(self.pet)
                self._last_autosave = time.time()
        self._timer = threading.Timer(TICK_SECONDS, self._tick)
        self._timer.daemon = True
        self._timer.start()

