class Pet:
    def __init__(self, name, pet_type):
        self.name        = name
        self.pet_type    = pet_type
        self.hunger      = 20.0
        self.happiness   = 80.0
        self.energy      = 90.0
        self.hygiene     = 100.0
        self.health      = 100.0
        self.age_hours   = 0.0
        self.created_at  = time.time()
        self.last_saved  = time.time()
        self.days_survived = 0
        self.is_sleeping = False
        self.is_sick     = False
        self.is_dead     = False
        self.event_log   = []

    @property
    def stage(self):
        s = STAGES[0][1]
        for h, name in STAGES:
            if self.age_hours >= h:
                s = name
        return s

    @property
    def mood(self):
        if self.is_dead:        return "Muerto"
        if self.is_sleeping:    return "Durmiendo"
        if self.is_sick:        return "Enfermo"
        if self.energy < 20:    return "Cansado"
        if self.hunger > 75:    return "Hambriento"
        if self.happiness < 30: return "Triste"
        if self.happiness > 70 and self.hunger < 35 and self.energy > 50:
            return "Feliz"
        return "Normal"

    @property
    def age_display(self):
        d = int(self.age_hours // 24)
        h = int(self.age_hours % 24)
        return f"{d}d {h}h" if d > 0 else f"{h}h"

    def add_event(self, msg):
        ts = time.strftime("%H:%M:%S")
        self.event_log.append(f"[{ts}] {msg}")
        if len(self.event_log) > 40:
            self.event_log.pop(0)

    def to_dict(self):
        return {
            "name": self.name, "pet_type": self.pet_type,
            "hunger": round(self.hunger, 2), "happiness": round(self.happiness, 2),
            "energy": round(self.energy, 2), "hygiene": round(self.hygiene, 2),
            "health": round(self.health, 2), "age_hours": round(self.age_hours, 4),
            "is_sleeping": self.is_sleeping, "is_sick": self.is_sick,
            "is_dead": self.is_dead, "created_at": self.created_at,
            "last_saved": time.time(), "days_survived": self.days_survived,
            "event_log": self.event_log[-30:]
        }

    @classmethod
    def from_dict(cls, d):
        p = cls(d["name"], d["pet_type"])
        for k in ["hunger","happiness","energy","hygiene","health","age_hours"]:
            setattr(p, k, float(d[k]))
        for k in ["is_sleeping","is_sick","is_dead"]:
            setattr(p, k, bool(d[k]))
        p.created_at     = float(d["created_at"])
        p.last_saved     = float(d["last_saved"])
        p.days_survived  = int(d.get("days_survived", 0))
        p.event_log      = list(d.get("event_log", []))
        return p


