class GameLogic:
    def __init__(self, pet):
        self.pet = pet

    def tick(self):
        p = self.pet
        if p.is_dead: return
        if p.is_sleeping:
            p.energy = min(100, p.energy + ENERGY_SLEEP_RATE)
            p.hunger = min(100, p.hunger + HUNGER_SLEEP_RATE)
            if p.energy >= WAKE_ENERGY:
                p.is_sleeping = False
                p.add_event(f"{p.name} se despertó descansado ☀️")
        else:
            p.hunger    = min(100, p.hunger    + HUNGER_RATE)
            p.happiness = max(0,   p.happiness - HAPPINESS_RATE)
            p.energy    = max(0,   p.energy    - ENERGY_RATE)
            p.hygiene   = max(0,   p.hygiene   - HYGIENE_RATE)

        p.age_hours    += TICK_SECONDS / 3600
        p.days_survived = int(p.age_hours / 24)

        if p.is_sick:
            p.health = max(0, p.health - HEALTH_SICK_DECAY)
        elif p.happiness > 60 and p.hunger < 40 and p.energy > 40:
            p.health = min(100, p.health + HEALTH_RECOVERY)

        if p.energy <= AUTO_SLEEP_ENERGY and not p.is_sleeping:
            p.is_sleeping = True
            p.add_event(f"{p.name} cayó dormido de agotamiento 💤")

        if not p.is_sick and p.hygiene < SICK_HYGIENE_TH and p.hunger > SICK_HUNGER_TH:
            p.is_sick = True
            p.add_event(f"{p.name} se enfermó! ¡Límpialo y aliméntalo!")
        if p.is_sick and p.hygiene > 65 and p.hunger < 30 and p.health > 50:
            p.is_sick = False
            p.add_event(f"{p.name} se recuperó solo gracias al buen cuidado 🌟")

        if p.health <= 0 and not p.is_dead:
            p.is_dead = True
            p.add_event(f"{p.name} ha fallecido 😢")

    def apply_offline(self, elapsed):
        p = self.pet
        if elapsed <= 0 or p.is_dead: return
        ticks = elapsed / TICK_SECONDS
        if p.is_sleeping:
            need  = WAKE_ENERGY - p.energy
            twake = max(0, need / ENERGY_SLEEP_RATE)
            if ticks <= twake:
                p.energy = min(100, p.energy + ENERGY_SLEEP_RATE * ticks)
                p.hunger = min(100, p.hunger + HUNGER_SLEEP_RATE * ticks)
            else:
                p.energy = WAKE_ENERGY
                p.hunger = min(100, p.hunger + HUNGER_SLEEP_RATE * twake)
                p.is_sleeping = False
                aw = ticks - twake
                p.hunger    = min(100, p.hunger    + HUNGER_RATE    * aw)
                p.happiness = max(0,   p.happiness - HAPPINESS_RATE * aw)
                p.energy    = max(0,   p.energy    - ENERGY_RATE    * aw)
                p.hygiene   = max(0,   p.hygiene   - HYGIENE_RATE   * aw)
        else:
            p.hunger    = min(100, p.hunger    + HUNGER_RATE    * ticks)
            p.happiness = max(0,   p.happiness - HAPPINESS_RATE * ticks)
            p.energy    = max(0,   p.energy    - ENERGY_RATE    * ticks)
            p.hygiene   = max(0,   p.hygiene   - HYGIENE_RATE   * ticks)

        p.age_hours    += elapsed / 3600
        p.days_survived = int(p.age_hours / 24)
        if p.is_sick:
            p.health = max(0, p.health - HEALTH_SICK_DECAY * ticks)
        elif p.happiness > 60 and p.hunger < 40 and p.energy > 40:
            p.health = min(100, p.health + HEALTH_RECOVERY * ticks)
        if not p.is_sick and p.hygiene < SICK_HYGIENE_TH and p.hunger > SICK_HUNGER_TH:
            p.is_sick = True
        if p.health <= 0 and not p.is_dead:
            p.is_dead = True

        mins = int(elapsed / 60)
        if mins >= 3:
            h = elapsed / 3600
            p.add_event(f"Estuviste {'%.1f' % h}h fuera." if h >= 1 else f"Estuviste {mins} min fuera.")

    # ── Acciones ──────────────────────────────────────────────────────────
    def _dead(self):
        return f"{self.pet.name} ya no está con nosotros 😢" if self.pet.is_dead else None

    def feed(self):
        e = self._dead()
        if e: return e
        p = self.pet
        if p.is_sleeping: return f"{p.name} está durmiendo."
        if p.hunger <= 5: return f"{p.name} ya está lleno!"
        p.hunger    = max(0,   p.hunger    - FEED_HUNGER)
        p.happiness = min(100, p.happiness + FEED_HAPPY)
        p.add_event(f"{p.name} comió y está satisfecho 🍖")
        return f"¡{p.name} comió!"

    def play(self):
        e = self._dead()
        if e: return e
        p = self.pet
        if p.is_sleeping: return f"{p.name} está durmiendo."
        if p.energy < 15: return f"{p.name} está muy cansado para jugar."
        p.happiness = min(100, p.happiness + PLAY_HAPPY)
        p.energy    = max(0,   p.energy    - PLAY_ENERGY)
        p.hunger    = min(100, p.hunger    + PLAY_HUNGER)
        p.add_event(f"{p.name} jugó y se divirtió mucho 🎮")
        return f"¡{p.name} jugó!"

    def toggle_sleep(self):
        e = self._dead()
        if e: return e
        p = self.pet
        if p.is_sleeping:
            p.is_sleeping = False
            p.add_event(f"{p.name} se despertó.")
            return f"{p.name} se despertó."
        if p.energy >= 90: return f"{p.name} no tiene sueño aún."
        p.is_sleeping = True
        p.add_event(f"{p.name} se fue a dormir 💤")
        return f"{p.name} se fue a dormir."

    def clean(self):
        e = self._dead()
        if e: return e
        p = self.pet
        if p.hygiene >= 95: return f"{p.name} ya está limpio."
        p.hygiene   = min(100, p.hygiene   + CLEAN_HYGIENE)
        p.happiness = min(100, p.happiness + CLEAN_HAPPY)
        p.add_event(f"{p.name} fue bañado y está fresco 🛁")
        return f"¡{p.name} fue limpiado!"

    def heal(self):
        e = self._dead()
        if e: return e
        p = self.pet
        if not p.is_sick and p.health >= 95: return f"{p.name} está sano."
        p.is_sick = False
        p.health  = min(100, p.health + HEAL_HEALTH)
        p.add_event(f"{p.name} recibió medicamento 💊")
        return f"¡{p.name} fue curado!"

    def pet_action(self):
        e = self._dead()
        if e: return e
        p = self.pet
        if p.is_sleeping: return f"No molestes a {p.name} mientras duerme."
        p.happiness = min(100, p.happiness + PET_HAPPY)
        p.energy    = max(0,   p.energy    - PET_ENERGY)
        p.add_event(f"{p.name} disfrutó el cariño ❤️")
        return f"¡Acariciaste a {p.name}!"


