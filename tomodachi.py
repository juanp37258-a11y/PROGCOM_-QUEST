# STORAGE
# ══════════════════════════════════════════════════════════════════════════════
def save_pet(pet):
    try:
        if os.path.exists(SAVE_FILE):
            import shutil; shutil.copy2(SAVE_FILE, SAVE_FILE + ".bak")
        with open(SAVE_FILE, "w", encoding="utf-8") as f:
            json.dump(pet.to_dict(), f, indent=2, ensure_ascii=False)
    except Exception as ex:
        print(f"[Save error] {ex}")

def load_pet():
    for fp in [SAVE_FILE, SAVE_FILE + ".bak"]:
        if not os.path.exists(fp): continue
        try:
            with open(fp, encoding="utf-8") as f:
                d = json.load(f)
            required = ["name","pet_type","hunger","happiness","energy",
                        "hygiene","health","age_hours","last_saved"]
            if all(k in d for k in required):
                return d
        except: pass
    return None


